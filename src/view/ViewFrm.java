    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // THanh toan
        DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
        int index = -1;
        index = jTable2.getSelectedRow();

        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Chọn dòng để thanh toán");

        }
        else if (dtm.getValueAt(index, 5).toString().trim().equals("Đã Thanh Toán") ) {
            JOptionPane.showMessageDialog(this, "Phòng đã thanh toán");
        }
        else {

            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
            LocalDateTime now = LocalDateTime.now();
            tblbill bill = new tblbill();

            bill.setIdBooking(Integer.parseInt(dtm.getValueAt(index, 6).toString()));
            bill.setPaymentType(jComboBox6.getSelectedItem().toString());
            bill.setNotes(jTextField7.getText());
            bill.setPaymentDate(dtf.format(now));
            String start = dtm.getValueAt(index, 3).toString();
            String end = dtm.getValueAt(index, 4).toString();

            try {
                //   Date Sdate1=new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").parse(start);
                Date Sdate1=new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").parse(start);
                Date Edate1=new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").parse(end);

                float sumCost = ( (Edate1.getTime()-Sdate1.getTime()) / (60*60*1000) ) *  Float.parseFloat(dtm.getValueAt(index, 2).toString())  ;
                bill.setAmount(sumCost);

                // Luu vao Database
                ThongKeDoanhThuDao tkdtd = new ThongKeDoanhThuDao();
                if (tkdtd.addBill(bill) !=-1) {
                    JOptionPane.showMessageDialog(this, " Thanh Toán Thành Công");
                    dtm.setValueAt("Đã Thanh Toán", index, 5);
                }
                else {
                    JOptionPane.showMessageDialog(this, " Thanh Toán Thất Bại");
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton4ActionPerformed

