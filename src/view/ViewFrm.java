    private void btnSubmitRoom1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitRoom1ActionPerformed

        searchFreeRoomDao sDao = new searchFreeRoomDao();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.getDataVector().removeAllElements();
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        LocalDateTime now = LocalDateTime.now();
        String timecurrent = dtf.format(now);
        String namehotel = "";
        try {

            if (jCheckBox1.isSelected()) {
                namehotel = "*";
            }
            else {

                namehotel = jComboBox2.getSelectedItem().toString();
            }
            List<tblRoom> list = sDao.timPhong(timecurrent, namehotel);
            System.out.println(list.size());
            if (list.size()==0) {
                JOptionPane.showMessageDialog(this, "Khach sạn "+ namehotel +" không còn phòng trống");
                dtm.getDataVector().removeAllElements();
                jTable1.updateUI();
            }
            else{
                for (tblRoom room : list) {
                    Vector<Object> v = new Vector<>();
                    v.add(room.getName());
                    v.add(room.getTypeString());
                    v.add(room.getDisplayPrice());
                    v.add(room.getDescription());
                    dtm.addRow(v);
                    jTable1.updateUI();
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSubmitRoom1ActionPerformed

